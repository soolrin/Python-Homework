# 18. Ввести число и определить, кратно ли оно 3, 5 и 7 одновременно.

print("\n")

num = int(input("Введите число: "))

print("\n==========================\n")

answer = "Кратное" if num % 3 == 0 and num % 5 == 0 and num % 7 == 0 else "Не кратное"
print(f"Число {num} {answer} 3, 5 и 7 одновременно.")

print("\n==========================\n")
