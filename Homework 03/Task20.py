# 20. Проверить, имеет ли введенное число дробную часть.

print("\n")

num = float(input("Введите число: "))

print("\n==========================\n")

palindrome = "Есть дробная часть" if num % 1 != 0 else "Нет дробной части"

print(f"У числа {num} {palindrome}")

print("\n==========================\n")
