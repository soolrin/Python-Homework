# 19. Ввести с клавиатуры пятизначное число и определить, является ли оно палиндромом.

print("\n")

num = int(input("Введите число: "))

print("\n==========================\n")

palindrome = "Палиндромом" if num // 10000 == num % 10 and num // 1000 % 10 == num % 100 // 10 else "Не палиндромом"

print(f"Число {num} {palindrome}")

print("\n==========================\n")
